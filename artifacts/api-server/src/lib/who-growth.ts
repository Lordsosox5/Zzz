function interpolateLMS(
  table: [number, number, number][],
  ageMonths: number,
  offset = 0,
): [number, number, number] | null {
  const idx = ageMonths - offset;
  if (idx < 0 || idx > table.length - 1) return null;
  const lo = Math.floor(idx);
  const hi = Math.min(lo + 1, table.length - 1);
  const frac = idx - lo;
  const [Ll, Ml, Sl] = table[lo];
  const [Lh, Mh, Sh] = table[hi];
  return [Ll + frac * (Lh - Ll), Ml + frac * (Mh - Ml), Sl + frac * (Sh - Sl)];
}

/** WHO LMS z-score formula (with adjustment for |z|>3) */
function lmsZ(x: number, L: number, M: number, S: number): number {
  let z: number;
  if (Math.abs(L) < 1e-6) {
    z = Math.log(x / M) / S;
  } else {
    z = (Math.pow(x / M, L) - 1) / (L * S);
  }
  // Adjust extreme values per WHO guidance
  if (z > 3) {
    const SD3pos = M * Math.pow(1 + L * S * 3, 1 / L);
    const SD23pos = SD3pos - M * Math.pow(1 + L * S * 2, 1 / L);
    z = 3 + (x - SD3pos) / SD23pos;
  } else if (z < -3) {
    const SD3neg = M * Math.pow(1 + L * S * (-3), 1 / L);
    const SD23neg = M * Math.pow(1 + L * S * (-2), 1 / L) - SD3neg;
    z = -3 + (x - SD3neg) / SD23neg;
  }
  return Math.round(z * 100) / 100;
}

/** Normal CDF → percentile (0-100) using Hart approximation */
export function zToPercentile(z: number): number {
  const t = 1 / (1 + 0.2316419 * Math.abs(z));
  const poly = t * (0.319381530 + t * (-0.356563782 + t * (1.781477937 + t * (-1.821255978 + t * 1.330274429))));
  const p = 1 - 0.3989422804 * Math.exp(-0.5 * z * z) * poly;
  const pct = z >= 0 ? p * 100 : (1 - p) * 100;
  return Math.round(pct * 10) / 10;
}

/** SD category label from z-score */
export function sdCategory(z: number): string {
  if (z < -3) return "severe";
  if (z < -2) return "moderate";
  if (z < -1) return "mild";
  if (z <= 1) return "normal";
  if (z <= 2) return "risk_overweight";
  if (z <= 3) return "overweight";
  return "obese";
}

/** Age in completed months between two ISO date strings */
export function ageInMonths(dob: string, measureDate: string): number {
  const d1 = new Date(dob);
  const d2 = new Date(measureDate);
  let months = (d2.getFullYear() - d1.getFullYear()) * 12 + (d2.getMonth() - d1.getMonth());
  if (d2.getDate() < d1.getDate()) months -= 1;
  return Math.max(0, months);
}

// ─── Public calculation API ───────────────────────────────────────────────────

export interface GrowthMetrics {
  bmi: number | null;
  weightZScore: number | null;
  weightPercentile: number | null;
  heightZScore: number | null;
  heightPercentile: number | null;
  bmiZScore: number | null;
  bmiPercentile: number | null;
  muacZScore: number | null;
  muacPercentile: number | null;
  sdCategory: string | null;
}

export function computeGrowthMetrics(
  weight: number | null | undefined,
  height: number | null | undefined,
  muac: number | null | undefined,
  sex: string,
  dob: string,
  measurementDate: string,
): GrowthMetrics {
  const age = ageInMonths(dob, measurementDate);
  const isBoy = sex.toLowerCase().startsWith("m");
  const wazTable = isBoy ? WAZ_B : WAZ_G;
  const hazTable = isBoy ? HAZ_B : HAZ_G;
  const bazTable = isBoy ? BAZ_B : BAZ_G;
  const muacTable = isBoy ? MUAC_B : MUAC_G;

  let bmi: number | null = null;
  if (weight && height) {
    const hm = height / 100;
    bmi = Math.round((weight / (hm * hm)) * 100) / 100;
  }

  function calc(
    x: number | null | undefined,
    table: [number, number, number][],
    offset = 0,
  ): { z: number; p: number } | null {
    if (!x) return null;
    const lms = interpolateLMS(table, age, offset);
    if (!lms) return null;
    const [L, M, S] = lms;
    const z = lmsZ(x, L, M, S);
    return { z, p: zToPercentile(z) };
  }

  const waz = calc(weight, wazTable);
  const haz = calc(height, hazTable);
  const baz = calc(bmi, bazTable);
  const muaz = age >= 3 ? calc(muac, muacTable, 3) : null;

  // Primary z-score for SD category: use WAZ (weight-for-age)
  const primaryZ = waz?.z ?? null;

  return {
    bmi,
    weightZScore: waz?.z ?? null,
    weightPercentile: waz?.p ?? null,
    heightZScore: haz?.z ?? null,
    heightPercentile: haz?.p ?? null,
    bmiZScore: baz?.z ?? null,
    bmiPercentile: baz?.p ?? null,
    muacZScore: muaz?.z ?? null,
    muacPercentile: muaz?.p ?? null,
    sdCategory: primaryZ !== null ? sdCategory(primaryZ) : null,
  };
}

/** Compute WHO centile reference values for charting (percentile curves) */
export function whoReferenceCurve(
  indicator: "weight" | "height" | "muac",
  sex: string,
  centile: number,
): { month: number; value: number }[] {
  const isBoy = sex.toLowerCase().startsWith("m");
  const Z_SCORES: Record<number, number> = {
    3: -1.8808, 15: -1.0364, 50: 0, 85: 1.0364, 97: 1.8808,
  };
  const z = Z_SCORES[centile] ?? 0;

  let table: [number, number, number][];
  let offset = 0;
  if (indicator === "weight") table = isBoy ? WAZ_B : WAZ_G;
  else if (indicator === "height") table = isBoy ? HAZ_B : HAZ_G;
  else { table = isBoy ? MUAC_B : MUAC_G; offset = 3; }

  const result: { month: number; value: number }[] = [];
  for (let i = 0; i < table.length; i++) {
    const [L, M, S] = table[i];
    let val: number;
    if (Math.abs(L) < 1e-6) {
      val = M * Math.exp(S * z);
    } else {
      val = M * Math.pow(1 + L * S * z, 1 / L);
    }
    result.push({ month: i + offset, value: Math.round(val * 10) / 10 });
  }
  return result;
}
