function valueAtZ(L: number, M: number, S: number, z: number): number {
  if (Math.abs(L) < 1e-6) return Math.round(M * Math.exp(S * z) * 10) / 10;
  return Math.round(M * Math.pow(1 + L * S * z, 1 / L) * 10) / 10;
}

export type WhoChartPoint = {
  month: number;
  p3: number; p15: number; p50: number; p85: number; p97: number;
};

export function getWhoReferenceCurve(
  indicator: "weight" | "height",
  sex: string,
): WhoChartPoint[] {
  const isBoy = sex.toLowerCase().startsWith("m");
  const table = indicator === "weight"
    ? (isBoy ? WAZ_B : WAZ_G)
    : (isBoy ? HAZ_B : HAZ_G);

  return table.map(([L, M, S], i) => ({
    month: i,
    p3:  valueAtZ(L, M, S, -1.8808),
    p15: valueAtZ(L, M, S, -1.0364),
    p50: valueAtZ(L, M, S, 0),
    p85: valueAtZ(L, M, S, 1.0364),
    p97: valueAtZ(L, M, S, 1.8808),
  }));
}

export function ageInMonths(dob: string, measureDate: string): number {
  const d1 = new Date(dob);
  const d2 = new Date(measureDate);
  let months = (d2.getFullYear() - d1.getFullYear()) * 12 + (d2.getMonth() - d1.getMonth());
  if (d2.getDate() < d1.getDate()) months -= 1;
  return Math.max(0, Math.min(60, months));
}

